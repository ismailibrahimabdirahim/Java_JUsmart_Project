private void loadStudents() {

    try {

        String faculty = cmbFaculty.getSelectedItem().toString();
        String batch = cmbBatch.getSelectedItem().toString();
        String className = cmbClass.getSelectedItem().toString();

        Connection conn = DatabaseHelper.getConnection();

        String sql =
        "SELECT s.StudentID, s.FullName " +
        "FROM Students s " +
        "JOIN Classes c ON s.ClassID = c.ClassID " +
        "JOIN Batches b ON c.BatchID = b.BatchID " +
        "JOIN Faculties f ON b.FacultyID = f.FacultyID " +
        "WHERE f.FacultyName=? " +
        "AND b.BatchName=? " +
        "AND c.ClassName=?";

        PreparedStatement pst = conn.prepareStatement(sql);

        pst.setString(1, faculty);
        pst.setString(2, batch);
        pst.setString(3, className);

        ResultSet rs = pst.executeQuery();

        DefaultTableModel model =
        (DefaultTableModel) tblStudents.getModel();

        model.setRowCount(0);

        while (rs.next()) {

            model.addRow(new Object[]{
                rs.getInt("StudentID"),
                rs.getString("FullName")
            });
        }

    } catch (Exception e) {

        JOptionPane.showMessageDialog(this, e);

    }
}
