    private void loadBatches() {

    try {

        Connection conn =
        DatabaseHelper.getConnection();

        String sql =
        "SELECT DISTINCT b.BatchName " +
        "FROM Batches b " +
        "JOIN Faculties f " +
        "ON b.FacultyID = f.FacultyID " +
        "WHERE f.FacultyName=?";

        PreparedStatement pst =
        conn.prepareStatement(sql);

        pst.setString(1,
        cmbFaculty.getSelectedItem().toString());

        ResultSet rs =
        pst.executeQuery();

        cmbBatch.removeAllItems();

        cmbBatch.addItem("Select Batch");

        while(rs.next()) {

            cmbBatch.addItem(
            rs.getString("BatchName"));
        }

        conn.close();

    } catch(Exception ex) {

        JOptionPane.showMessageDialog(this,
        ex.getMessage());
    }
}

