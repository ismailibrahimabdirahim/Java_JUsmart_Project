    private void loadClasses() {

    try {

        Connection conn =
        DatabaseHelper.getConnection();

        String sql =
        "SELECT DISTINCT c.ClassName " +
        "FROM Classes c " +
        "JOIN Batches b " +
        "ON c.BatchID = b.BatchID " +
        "JOIN Faculties f " +
        "ON b.FacultyID = f.FacultyID " +
        "WHERE b.BatchName=? " +
        "AND f.FacultyName=?";

        PreparedStatement pst =
        conn.prepareStatement(sql);

        pst.setString(1,
        cmbBatch.getSelectedItem().toString());

        pst.setString(2,
        cmbFaculty.getSelectedItem().toString());

        ResultSet rs =
        pst.executeQuery();

        cmbClass.removeAllItems();

        cmbClass.addItem("Select Class");

        while(rs.next()) {

            cmbClass.addItem(
            rs.getString("ClassName"));
        }

        conn.close();

    } catch(Exception ex) {

        JOptionPane.showMessageDialog(this,
        ex.getMessage());
    }
}
