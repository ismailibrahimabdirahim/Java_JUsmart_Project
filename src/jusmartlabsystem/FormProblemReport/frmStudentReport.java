    private boolean validateForm() {

    // Faculty Validation
    
    if(cmbFaculty.getSelectedIndex() == 0) {

        JOptionPane.showMessageDialog(this,
        "Please Select Faculty");

        cmbFaculty.requestFocus();

        return false;
    }

    // Batch Validation
    if(cmbBatch.getSelectedIndex() == 0) {

        JOptionPane.showMessageDialog(this,
        "Please Select Batch");

        cmbBatch.requestFocus();

        return false;
    }

    // Class Validation
    if(cmbClass.getSelectedIndex() == 0) {

        JOptionPane.showMessageDialog(this,
        "Please Select Class");

        cmbClass.requestFocus();

        return false;
    }

    // Student Validation
    if(txtName.getText().trim().isEmpty()) {

        JOptionPane.showMessageDialog(this,
        "Please Select Student");

        txtName.requestFocus();

        return false;
    }
    String enteredName =
txtName.getText().trim();

boolean studentFound = false;

for(int i = 0; i < tblStudents.getRowCount(); i++) {

    String tableName =
    tblStudents.getValueAt(i, 1).toString();

    if(tableName.equalsIgnoreCase(enteredName)) {

        studentFound = true;
        break;
    }
}

if(!studentFound) {

    JOptionPane.showMessageDialog(this,
    "You are not registered student");

    txtName.requestFocus();

    return false;
}

    // SERVER VALIDATION
    if(cmbServerNo.getSelectedIndex() == 0) {

        JOptionPane.showMessageDialog(this,
        "Please Select Server");

        cmbServerNo.requestFocus();

        return false;
    }

    // PC VALIDATION
    if(txtComputerNo.getText().trim().isEmpty()) {

        JOptionPane.showMessageDialog(this,
        "Please Enter Computer Number");

        txtComputerNo.requestFocus();

        return false;
    }

    // GET PC NUMBER
    String pcText =
    txtComputerNo.getText().trim();

    pcText = pcText.replace("PC", "");
    pcText = pcText.replace("pc", "");
    pcText = pcText.trim();

    int pcNo;

    try {

        pcNo = Integer.parseInt(pcText);

    } catch(Exception e) {

        JOptionPane.showMessageDialog(this,
        "Invalid PC Number");

        txtComputerNo.requestFocus();

        return false;
    }

    // SERVER RANGE VALIDATION
    int server =
    cmbServerNo.getSelectedIndex();

    boolean valid = false;

    // SERVER 1
    if(server == 1) {

        if(pcNo >= 1 && pcNo <= 28) {

            valid = true;
        }
    }

    // SERVER 2
    else if(server == 2) {

        if(pcNo >= 29 && pcNo <= 56) {

            valid = true;
        }
    }

    // SERVER 3
    else if(server == 3) {

        if(pcNo >= 57 && pcNo <= 84) {

            valid = true;
        }
    }

    // INVALID RANGE
    if(!valid) {

        JOptionPane.showMessageDialog(this,
        "PC Number does not belong to selected Server");

        txtComputerNo.requestFocus();

        return false;
    }

    // PROBLEM TYPE VALIDATION
    if(cmbProblemType.getSelectedIndex() == 0) {

        JOptionPane.showMessageDialog(this,
        "Please Select Problem Type");

        cmbProblemType.requestFocus();

        return false;
    }

    return true;
}
    
    private void submitProblem() {

    try {

        Connection conn =
        DatabaseHelper.getConnection();

        // GET STUDENT ID
        int studentID = 0;

        for(int i = 0; i < tblStudents.getRowCount(); i++) {

            String name =
            tblStudents.getValueAt(i, 1).toString();

            if(name.equalsIgnoreCase(
            txtName.getText().trim())) {

                studentID =
                Integer.parseInt(
                tblStudents.getValueAt(i, 0).toString());

                break;
            }
        }

        String sql =
        "INSERT INTO problemreports " +
        "(ProblemType, ProblemDescription, StudentID, ComputerNumber, ServerNo) " +
        "VALUES (?, ?, ?, ?, ?)";

        PreparedStatement pst =
        conn.prepareStatement(sql);

        pst.setString(1,
        cmbProblemType.getSelectedItem().toString());

        pst.setString(2,
        txtProblemDesc.getText());

        pst.setInt(3,
        studentID);

        pst.setString(4,
        txtComputerNo.getText());

        String serverText =
        cmbServerNo.getSelectedItem().toString();

        serverText =
        serverText.replace("Server", "").trim();

        pst.setInt(5,
        Integer.parseInt(serverText));

        pst.executeUpdate();

        JOptionPane.showMessageDialog(this,
        "Problem Submitted Successfully");

        conn.close();

    }

    catch (Exception e) {

        javax.swing.JOptionPane.showMessageDialog(this, e);

    }
}
