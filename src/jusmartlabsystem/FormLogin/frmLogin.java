   public void check() {

    try {

        String uname = txtUserName.getText().trim().toLowerCase();
        String pwd = txtPassword.getText().trim();
        String role =
        cmbRoles.getSelectedItem().toString();

        Connection conn = DatabaseHelper.getConnection();

        String sql = "SELECT * FROM Admins WHERE Email=? AND Password=? AND Role=?";

        PreparedStatement pst = conn.prepareStatement(sql);

        pst.setString(1, uname);
        pst.setString(2, pwd);
        pst.setString(3, role);

        ResultSet rs = pst.executeQuery();

        if (rs.next()) {

        frmAdminDashboard2.adminId =
        rs.getInt("AdminID");

        frmAdminDashboard2.adminEmail =
        rs.getString("Email");

        frmAdminDashboard2.adminRole =
        rs.getString("Role");

        frmAdminDashboard2 frmAdmindash =
        new frmAdminDashboard2();

        frmAdmindash.setVisible(true);

        this.dispose();

    } else {

        JOptionPane.showMessageDialog(
            this,
            "Invalid Email or Password!",
            "Login Failed",
            JOptionPane.ERROR_MESSAGE
        );

        txtUserName.setText("");
        txtPassword.setText("");

        txtUserName.requestFocus();
    }

        rs.close();
        pst.close();
        conn.close();

    } catch (Exception ex) {

        JOptionPane.showMessageDialog(this, ex.getMessage());

    }
}
        
